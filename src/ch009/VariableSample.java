package ch009;

/**
 * 演示变量的声明
 * @author 程序猿大头
 *
 */
public class VariableSample {

	public static void main(String[] args) {
		/**
		 * 定义了一个int类型的变量a
		 */
		int a;

		/**
		 * 定义了一个int类型的变量b，并且给予初始值1 =为赋值运算符，用于把=右边的表达式赋给左边的变量
		 */
		int b = 1;

		/**
		 * 同时定义int类型变量c、d、e并给e初始值5
		 */
		int c, d, e = 5;
	}

}
